public class Calcul {
	/**
	 * Résultat de l'addition du parametre a avec le parametre b
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Résultat de la soustraction du paremetre a avec la parametre b
	 * @param a
	 * @param b
	 * @return a-b
	 */	
	public int soustraction(int a, int b){
		return a-b;
	}
}
